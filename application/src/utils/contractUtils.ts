const contractAddress = import.meta.env.VITE_CONTRACT_ADDRESS;

interface NFTData {
  tokenId: bigint;
  creator: string;
  owner: string;
  name: string;
  description: string;
  imageURI: string;
  price: bigint;
  listed: boolean;
}

interface SonicNFTContract extends ethers.BaseContract {
  mintNFT(name: string, description: string, imageURI: string, price: bigint): Promise<ethers.ContractTransactionResponse>;
  buyNFT(tokenId: number, options: { value: bigint }): Promise<ethers.ContractTransactionResponse>;
  getAllListedNFTs(): Promise<NFTData[]>;
  getAllNFTs(): Promise<NFT[]>;
  updatePrice(tokenId: number, newPrice: bigint): Promise<ethers.ContractTransactionResponse>;
}

export const getContract = async (withSigner = false): Promise<SonicNFTContract> => {
  if (!window.ethereum) {
    throw new Error('Please install MetaMask');
  }

  const provider = new ethers.BrowserProvider(window.ethereum);
  const contract = new ethers.Contract(
    contractAddress,
    contractAbi,
    provider
  );

  if (withSigner) {
    const signer = await provider.getSigner();
    return contract.connect(signer) as SonicNFTContract;
  }

  return contract as SonicNFTContract;
};

export const mintNFT = async (name: string, description: string, imageURI: string, price: number) => {
  const contract = await getContract(true);
  const priceInWei = ethers.parseEther(price.toString());
  
  const tx = await contract.mintNFT(name, description, imageURI, priceInWei);
  const receipt = await tx.wait();
  
  return receipt;
};

export const buyNFT = async (tokenId: number, options: { value: bigint }) => {
  const contract = await getContract(true);
  
  const tx = await contract.buyNFT(tokenId, options);
  const receipt = await tx.wait();
  
  return receipt;
};

export const getAllListedNFTs = async () => {
  const contract = await getContract();
  const nfts = await contract.getAllListedNFTs();
  
  return nfts.map((nft: NFTData) => ({
    id: nft.tokenId.toString(),
    name: nft.name,
    description: nft.description,
    imageUrl: nft.imageURI,
    price: ethers.formatEther(nft.price),
    seller: nft.owner,
    creator: nft.creator,
    listed: nft.listed
  }));
};

export const updateNFTPrice = async (tokenId: number, newPrice: number) => {
  const contract = await getContract(true);
  const priceInWei = ethers.parseEther(newPrice.toString());
  
  const tx = await contract.updatePrice(tokenId, priceInWei);
  const receipt = await tx.wait();
  
  return receipt;
};

export interface NFT {
  tokenId: string;
  creator: string;
  owner: string;
  name: string;
  description: string;
  imageUrl: string;
  price: string;
  listed: boolean;
}

export const getAllNFTs = async (): Promise<NFT[]> => {
  const contract = await getContract();
  try {
    const nfts = await contract.getAllNFTs();
    return nfts.map((nft: any) => ({
      tokenId: nft.tokenId.toString(),
      creator: nft.creator,
      owner: nft.owner,
      name: nft.name,
      description: nft.description,
      imageUrl: nft.imageURI,
      price: ethers.formatEther(nft.price),
      listed: nft.listed
    }));
  } catch (error) {
    console.error('Error fetching all NFTs:', error);
    throw error;
  }
};